import 'plasterboard_genetic_solver.dart';

Future<void> main(List<String> args) async {
  final writeReport = args.contains('--write-report');
  final corpus = loadPlasterSolverBenchmarkCorpus();
  final materials = _defaultMaterials();
  final buffer = StringBuffer()
    ..writeln('# Plasterboard Genetic Algorithm Report')
    ..writeln()
    ..writeln(
      'Generated by `dart run tool/plasterboard_genetic_benchmark.dart`.',
    )
    ..writeln()
    ..writeln('## Configuration')
    ..writeln()
    ..writeln('- Population: 56')
    ..writeln('- Generations: 70')
    ..writeln('- Elite count: 8')
    ..writeln('- Mutation rate: 0.24')
    ..writeln('- Seed: 1309')
    ..writeln('- Crossover unit: whole run')
    ..writeln('- Primary fitness metric: cut waste percentage')
    ..writeln()
    ..writeln('## Results')
    ..writeln()
    ..writeln(
      '| Fixture | Beam waste | GA waste | Waste delta | Beam sheets | '
      'GA sheets | GA candidates | GA time |',
    )
    ..writeln('|---|---:|---:|---:|---:|---:|---:|---:|');

  var totalBeamWaste = 0.0;
  var totalGeneticWaste = 0.0;
  var totalBeamSheets = 0;
  var totalGeneticSheets = 0;
  var improved = 0;
  var same = 0;
  var worse = 0;

  for (final scenario in corpus.scenarios) {
    final beamLayouts = PlasterGeometry.calculateLayout(
      scenario.shapes,
      materials,
    );
    final beamTakeoff = PlasterGeometry.calculateTakeoff(
      scenario.shapes,
      beamLayouts,
      0,
    );
    final genetic = GeneticPlasterSolver.solve(
      roomShapes: scenario.shapes,
      materials: materials,
      config: const GeneticPlasterSolverConfig(
        populationSize: 56,
        generations: 70,
        eliteCount: 8,
        mutationRate: 0.24,
      ),
    );

    final delta =
        genetic.takeoff.estimatedWastePercent -
        beamTakeoff.estimatedWastePercent;
    if (delta < -0.01) {
      improved++;
    } else if (delta > 0.01) {
      worse++;
    } else {
      same++;
    }

    totalBeamWaste += beamTakeoff.estimatedWastePercent;
    totalGeneticWaste += genetic.takeoff.estimatedWastePercent;
    totalBeamSheets += beamTakeoff.totalSheetCount;
    totalGeneticSheets += genetic.takeoff.totalSheetCount;

    buffer.writeln(
      '| ${scenario.name} | '
      '${beamTakeoff.estimatedWastePercent.toStringAsFixed(2)}% | '
      '${genetic.takeoff.estimatedWastePercent.toStringAsFixed(2)}% | '
      '${_formatSigned(delta)} pp | '
      '${beamTakeoff.totalSheetCount} | '
      '${genetic.takeoff.totalSheetCount} | '
      '${genetic.evaluatedCandidates} | '
      '${genetic.elapsedMs} ms |',
    );
  }

  final count = corpus.scenarios.length;
  buffer
    ..writeln()
    ..writeln('## Summary')
    ..writeln()
    ..writeln(
      '- Average beam waste: '
      '${(totalBeamWaste / count).toStringAsFixed(2)}%',
    )
    ..writeln(
      '- Average GA waste: '
      '${(totalGeneticWaste / count).toStringAsFixed(2)}%',
    )
    ..writeln(
      '- Average waste delta: '
      '${_formatSigned(totalGeneticWaste / count - totalBeamWaste / count)} '
      'percentage points',
    )
    ..writeln('- Beam total sheets: $totalBeamSheets')
    ..writeln('- GA total sheets: $totalGeneticSheets')
    ..writeln('- Fixtures improved / same / worse: $improved / $same / $worse')
    ..writeln()
    ..writeln('## Notes')
    ..writeln()
    ..writeln(
      '- The GA decodes one complete layout per wall or ceiling; runs remain '
      'the crossover unit inside the surface genome.',
    )
    ..writeln(
      '- The deterministic beam result is included in the initial population, '
      'so regressions generally indicate decode/model mismatch rather than GA '
      'selection pressure.',
    )
    ..writeln(
      '- Isolates are not used yet. The measured fixture runtimes are the '
      'basis for deciding whether isolate overhead is worthwhile.',
    );

  final report = buffer.toString();
  stdout.write(report);
  if (writeReport) {
    await File(
      'doc/plasterboard_genetic_algorithm_report.md',
    ).writeAsString(report);
  }
}

String _formatSigned(double value) =>
    '${value >= 0 ? '+' : ''}${value.toStringAsFixed(2)}';

